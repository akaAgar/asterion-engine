        /// The sixth extra mouse button.
        /// </summary>
        Button6 = 8,
        /// <summary>
        /// The seventh extra mouse button.
        /// </summary>
        Button7 = 9,
        /// <summary>
        /// The eigth extra mouse button.
        /// </summary>
        Button8 = 10,
